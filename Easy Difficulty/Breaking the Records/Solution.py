def breakingRecords(scores):
    least=high=0
    min=max=scores[0]
    for i in scores:
        if min>i:
            min=i
            least+=1
        if max<i:
            max=i
            high+=1
    return [high,least]

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

